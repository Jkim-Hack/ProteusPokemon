void Player::draw_player(int type) {

    //0 = face down
    //1 = face up
    //2 = face left
    //3 = walk down
    //4 = walk up
    //5 = walk horiz

    //0 = black 20 22 20
    //1 = green 4 254 4 change to transparent
    //2 = gray 156 154 156
    //3 = white 236 234 236
    //4 = light gray 180 178 180

    int(*pixels)[256];
    switch (type){
        case 0: //face down
            pixels = &this->sprite0;
            break;
        case 1: //face up
            pixels = &this->sprite1;
            break;
        case 2: //face left
            pixels = &this->sprite2;
            break;
        case 3: //walk down
            pixels = &this->sprite3;
            break;
        case 4: //walk up
            pixels = &this->sprite4;
            break;
        case 5: //walk left
            pixels = &this->sprite5;
            break;
        case 6: //face right
            pixels = &this->sprite2;
            reverse_pixels(pixels);
            break;
        case 7: //walk right
            pixels = &this->sprite5;
            reverse_pixels(pixels);
            break;
    }

    //i = height //j = width
    for (int i = 0; i < 16; ++i) {
        for (int j = 0; j < 16; ++j) {
            int pixel = *pixels[j + (i*16)];
            switch (pixel) {
                case 0:
                    LCD.SetFontColor(0x141614);
                    LCD.DrawPixel(this->originX + j, this->originY + i);
                    break;
                case 1:
                    break;
                case 2:
                    LCD.SetFontColor(0x9C9A9C);
                    LCD.DrawPixel(this->originX + j, this->originY + i);
                    break;
                case 3:
                    LCD.SetFontColor(0xECEAEC);
                    LCD.DrawPixel(this->originX + j, this->originY + i);
                    break;
                case 4:
                    LCD.SetFontColor(0xB4B2B4);
                    LCD.DrawPixel(this->originX + j, this->originY + i);
                    break;
            }
        }
    }

}

void Player::reverse_pixels(int (*pixels)[256]) {

    for (int i = 0; i < 16; ++i) {
        for (int j = 0; j < 8; ++j) {
            int temp = *pixels[(i*16)+j];
            *pixels[(i*16) + j] = *pixels[(i*16) + (8-j-1)];
            *pixels[(i*16) + (8-j-1)] = temp;
        }
    }
}

